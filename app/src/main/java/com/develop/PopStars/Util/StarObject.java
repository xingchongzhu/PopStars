public class StarObject  implements Serializable{
	private float x;
	private float y;
	private int color;
	private boolean live;
	
	public StarObject(float x,float y,int color,boolean live){
		this.x = x;
		this.y = y;
		this.color = color;
		this.live = live;
	}
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	public int getColor() {
		return color;
	}
	public void setColor(int color) {
		this.color = color;
	}
	public boolean getLive() {
		return live;
	}
	public void setLive(boolean live) {
		this.live = live;
	}
   
}
